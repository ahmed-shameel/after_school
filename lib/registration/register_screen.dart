            const SizedBox(
              height: 60,
            ),
            Form(
              key: _formKey,
              child: Column(
                children: [
                  Row(
                    children: [
                      Expanded(
                        child: TextFormField(
                          validator: (value) =>
                          EmailValidator.validate(value!)
                              ? null
                              : "Please enter a valid email",
